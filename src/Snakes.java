public class Snakes
{
    String breed;
    String length;
    String venomous;

    public Snakes(String breed, String length, String venomous)
    {
        this.breed = breed;
        this.length = length;
        this.venomous = venomous;
    }

    public String getBreed()
    {
        return breed;
    }

    public String getLength()
    {
        return length;
    }

    public String getVenomous()
    {
        return venomous;
    }
}
